public class Book {
    //for Encapsulation
    private int isbn;
    private String title;
    private String category;
    private String author;
    public void setIsbn(int isbn){
        this.isbn=isbn;
    }
    public int getIsbn(){
        return isbn;
    }
    public void setTitle(int isbn){
        this.title=title;
    }
    public String getTitle(){
        return title;
    }
    public void setCategory(int isbn){
        this.category=category;
    }
    public String getCategory(){
        return category;
    }
    public void setAuthor(int isbn){
        this.author=author;
    }
    public String getAuthor(){
        return author;
    }


}
